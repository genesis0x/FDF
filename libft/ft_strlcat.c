#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dlen;
	size_t	slen;

	slen = ft_strlen(src);
	if (size == 0)
		return (slen + size);
	i = 0;
	dlen = ft_strlen(dst);
	if (dlen >= size)
		return (size + slen);
	while (src[i] && dlen + i < (size - 1))
	{
		dst[dlen + i] = src[i];
		i++;
	}
	dst[dlen + i] = '\0';
	return (slen + dlen);
}
